public class Parrot {
    private int kg;
    private String paroda;
    private String aet;

    public Parrot(int kg, String paroda, String aet) {
        this.kg = kg;
        this.paroda = paroda;
        this.aet = aet;
    }

    public int getKg() {
        return kg;
    }

    public void setKg(int kg) {
        this.kg = kg;
    }

    public String getParoda() {
        return paroda;
    }

    public void setParoda(String paroda) {
        this.paroda = paroda;
    }

    public String getAet() {
        return aet;
    }

    public void setAet(String aet) {
        this.aet = aet;
    }
}

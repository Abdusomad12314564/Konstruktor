public class Lion {
    private int kg;
    private int length;
    private String aet;

    public Lion(int kg, int length, String aet) {
        this.kg = kg;
        this.length = length;
        this.aet = aet;
    }

    public int getKg() {
        return kg;
    }

    public void setKg(int kg) {
        this.kg = kg;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public String getAet() {
        return aet;
    }

    public void setAet(String aet) {
        this.aet = aet;
    }
}
